package com.example.prodecttestapp.model;

public class sort_props {
    private String A;

    private String B;

    private String C;

    public sort_props(String a, String b, String c) {
        A = a;
        B = b;
        C = c;
    }

    public String getA() {
        return A;
    }

    public void setA(String a) {
        A = a;
    }

    public String getB() {
        return B;
    }

    public void setB(String b) {
        B = b;
    }

    public String getC() {
        return C;
    }

    public void setC(String c) {
        C = c;
    }
}
